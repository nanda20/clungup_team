function format_tgl($tanggal) {
  $bulan = array('Bulan', 'Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni', 'Juli', 'Agustus', 'September', 'Oktober', 'Novermber', 'Desember');

  $cal = explode("-",$tanggal);
  $tgl = $cal[2];
  $bln = $cal[1];
  $thn = $cal[0];

  for ($i=0; $i < 13; $i++) { 
    if ($bln==$i) {
      $bln=$bulan[$i];
    }
  }

  echo "$tgl $bln $thn";
}
